package org.example;

public class Calculator {

    //Homework 5, point 3
    public int calculateSumOfTwoIntegerValue(int firstValue, int secondValue){

        return firstValue + secondValue;
    }
    public int calculateSubtractionBetweenTwoIntegerValues(int firstValue, int secondValue){

        return firstValue - secondValue;
    }
    //Homework 5, point 4
    public boolean theFirstValueIsBiggerThanTheSecondValue(int firstValue, int secondValue){
        return firstValue > secondValue;
    }

    // Homework 6, point 1
    public double calculateDivisionsOfFloatingPointNumbers(double firstValue, double secondValue){
        return firstValue / secondValue;
    }

    public double calculateMultiplicationsOfFloatingPointNumbers(double firstValue, double secondValue){
        return firstValue * secondValue;
    }

}
